package com.bridgelabz.basics;

public class SwapTwoNumbers {

	public static void main(String[] args) {
		int i = 0;
		int j= 1;
		int temp;
		System.out.println("i = " + i + "	j = " + j);
		
		temp =i;
		i = j;
		j = temp;
		System.out.println("After swapping...");
		System.out.println("i = " + i + "	j = " + j);
			
	}

}
